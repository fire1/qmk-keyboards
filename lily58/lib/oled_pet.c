// Shortcut to check layer
static bool is_layer_eql(uint16_t state) {
    if (layer_state == state) {
        return true;
    }
    return false;
}

static void print_master_narrow(void) {
    /* Print current mode */

    if (timer_elapsed(startup_timer) > 5000 && !isInitRun) {
        isInitRun = true;
    }

    if (isInitRun) {
        // lyli logo
        oled_set_cursor(0, 0);
        render_logo();
    } else {
        // initial USB info
        oled_set_cursor(2, 0);
        oled_write_char((char)30, false);

        oled_set_cursor(0, 2);
        if (keymap_config.swap_lctl_lgui) {
            oled_write_raw_P(mac_logo, sizeof(mac_logo));
        } else {
            oled_write_raw_P(windows_logo, sizeof(windows_logo));
        }
    }

    oled_set_cursor(0, 5);

    switch (layer_state) {
        case L_BASE:
            if (led_usb_state.caps_lock) {
                oled_write(("CPSLK"), true);
            } else {
                oled_write(("QWRTY"), false);
            }
            break;
        case L_ADJUST:
        case L_ADJUST_TRI:
            oled_write(("ADJST"), true);
            break;

        case L_LOWER:
        case L_RAISE:
            // empty
            break;

        default:
            oled_write(("UNDEF"), false);
            break;
    }

    oled_set_cursor(0, 6);
    oled_write("-----", false);

    oled_set_cursor(0, 7);
    oled_write(("LOWER"), is_layer_eql(L_LOWER));
    oled_set_cursor(0, 8);
    oled_write(("RAISE"), is_layer_eql(L_RAISE));

    /* Print current key */
    oled_set_cursor(0, 11);
    oled_write(keylog_str, false);

    render_luna(0, 13);
}

