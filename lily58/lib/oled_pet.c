void set_oled_keys(uint16_t keycode, keyrecord_t *record) {
    /* KEYBOARD PET STATUS START */
    switch (keycode) {
        case KC_LCTL:
        case KC_RCTL:
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
            break;
        case KC_SPC:
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;
            /* KEYBOARD PET STATUS END */
    }

    if (record->event.pressed) {
        draw_screen_keys(keycode, record);
    }
}

